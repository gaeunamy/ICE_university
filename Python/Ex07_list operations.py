# 덧셈 연ㄴ사
color1 = ['red', 'blue', 'green']
color2 = ['orange', 'black', 'white']
print(color1 + color2) # 두 리스트를 합침

print(len(color1)) # 리스트 원소 개수 출력

total_color = color1 + color2
print(total_color) # 합쳐진 값이 할당됨

# 곱셈 연산
print(color1 * 2) # color 리스트 2번 반복

# in 연산 / 존재 여부 확인
print('blue' in color2) # 존재하지 않으므로 False / True or False로 출력